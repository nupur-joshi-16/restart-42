# subscripting
print("Nupur"[0]) # First character
print("Hello world!"[-1]) # Last character

# String
print("123" + "456")

# Integer = Whole number
print(123 + 456)

# Large numbers
print(2_50_00_000) # 2 crore 50 lakhs

# Float or Floating Point Number
print(123.45)

# Boolean
print(True)
print(False) # T and F must be in capital

