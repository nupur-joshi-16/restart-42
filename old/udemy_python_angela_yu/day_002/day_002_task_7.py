# Assignment operators

score = 30
print(score)

score += 1 # score = score + 1
print(score) # 30 + 1 = 31

score -=1 # score = score - 1
print(score) # 31 - 1 = 30

score *= 2 # score = score * 2
print(score) # 30 * 2 = 60

score /= 2 # score = score / 2
print(score) # 60 / 2 = 30.0

# f-string
#print("Your score is " + score) # TypeError
print(f"Your score is = {score}") # f-string



