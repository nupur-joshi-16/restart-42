# Mathematical Operations
print(20 + 10) #addition
print(20 - 10) #subtraction
print(5 * 5) #multiplication
print(10 / 2) #division - answer is always in floating point number. Implicit type casting
print(10 // 2) #answer is always in integer
print(10 / 3)
print(10 // 3) 
print(3 ** 3) #exponent or 3 to power of 3


#Order of operations
# PEMDAS, left to right

print (3 * 3 + 3 / 3 - 3)
# 9 + 3 / 3 -3
# 9 + 1.0 - 3
# 10.0 - 3
# 7.0

print(3 / 3 + 3 * 3 - 3)
# 1.0 + 3 * 3 - 3
# 1.0 + 9 - 3
# 10.0 - 3
#7.0

print(3 / 3 - 3 * 3 + 3)
# 1.0 - 3 * 3 + 3
# 1.0 - 9 + 3
# -8.0 + 3
# -11.0

print(-8.0 + 3)

print (3 * (3 + 3) / 3 - 3)
# 3 * 6 / 3 - 3
# 18 / 3 - 3
# 6 - 3
# 3





